package Codechef_1_Oct_Long_Challenge;

import java.util.Scanner;

public class CP_1_Podium_Finish {
    public static void main(String[] args){
        // Podium Finish
        Scanner sc=new Scanner(System.in);
        int sum=0;
        int t=sc.nextInt();
        while(t-->0){
            int a=sc.nextInt();
            int b=sc.nextInt();
            sum=a+b;
            System.out.println(sum);
        }
    }
}

